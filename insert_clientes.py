def insert_clientes():
    with app.app_context():
        total_inseridos = 0
        total_existentes = 0
        
        for cliente_data in clientes_data:
            # Limpa os espaços extras dos dados
            codigo = cliente_data['codigo'].strip()
            nome = cliente_data['nome'].strip()
            
            # Verifica se o cliente já existe
            existing = Cliente.query.filter_by(codigo=codigo).first()
            if not existing:
                cliente = Cliente(
                    codigo=codigo,
                    nome=nome
                )
                db.session.add(cliente)
                total_inseridos += 1
            else:
                total_existentes += 1
        
        try:
            db.session.commit()
            print(f"Importação concluída com sucesso!")
            print(f"Total de clientes inseridos: {total_inseridos}")
            print(f"Total de clientes já existentes: {total_existentes}")
        except Exception as e:
            db.session.rollback()
            print(f"Erro ao inserir clientes: {str(e)}")

if __name__ == '__main__':
    insert_clientes()
